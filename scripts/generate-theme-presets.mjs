import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

import YAML from 'yaml';

const scriptDir = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(scriptDir, '..');
const themesDir = path.join(repoRoot, 'themes');
const outputDir = path.join(repoRoot, 'src', 'generated');
const outputFile = path.join(outputDir, 'theme-presets.ts');

function listThemeDirs() {
  if (!fs.existsSync(themesDir)) return [];
  return fs.readdirSync(themesDir, { withFileTypes: true })
    .filter((entry) => entry.isDirectory())
    .map((entry) => entry.name)
    .sort((a, b) => a.localeCompare(b));
}

function readThemeConfig(name) {
  const configPath = path.join(themesDir, name, 'config.yaml');
  if (!fs.existsSync(configPath)) return null;
  return YAML.parse(fs.readFileSync(configPath, 'utf8'));
}

const presets = {};
for (const name of listThemeDirs()) {
  const config = readThemeConfig(name);
  if (config) {
    presets[name] = config;
  }
}

const content = `// This file is auto-generated by scripts/generate-theme-presets.mjs. Do not edit manually.
export const THEME_PRESETS = ${JSON.stringify(presets, null, 2)} as const;
`;

fs.mkdirSync(outputDir, { recursive: true });
fs.writeFileSync(outputFile, content);
